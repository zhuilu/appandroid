package com.xinniu.android.qiqueqiao.bean;

public class ConnectPersonBean {
    private String name;
    private String mobile;

    public ConnectPersonBean(String name, String mobile) {
        this.name = name;
        this.mobile = mobile;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }
}
