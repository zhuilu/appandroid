    public static String cleanFirstUnderline(String str) {
        if (TextUtils.isEmpty(str)) {
            return "";
        }
        if (str.substring(0, 1).equals("_")) {
            return str.substring(1, str.length());
        } else {
            return str;
        }
    }

    public static String reverseString(String str) {
        if (TextUtils.isEmpty(str)) {
            return "";
        }
        String[] afterStr = str.split("_");
        String newStr = "";
        for (int i = afterStr.length - 1; i >= 0; i--) {
            if (i != afterStr.length - 1) {
                newStr = newStr + "_";
                newStr = newStr + afterStr[i];
            } else {
                newStr = newStr + afterStr[i];
            }
        }
        return newStr;
    }

